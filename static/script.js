let web3;
if (window.ethereum) {
    web3 = new Web3(window.ethereum);
    try {
        window.ethereum.enable().then(() => {
            console.log('DApp connected');
        });
    } catch (e) {
        console.error('User denied access request');
    }
} else if (window.web3) {
    web3 = new Web3(window.web3.currentProvider);
} else {
    console.error('Please install MetaMask!');
}

let contract = new web3.eth.Contract(contractABI, contractAddress);

document.getElementById('mintForm').addEventListener('submit', async (event) => {
    event.preventDefault();
    const address = document.getElementById('mintAddress').value;
    const id = document.getElementById('mintId').value;
    const amount = document.getElementById('mintAmount').value;

    const accounts = await web3.eth.requestAccounts();
    const mintFee = await contract.methods.mintFee().call();
    const totalFee = mintFee * amount;

    contract.methods.mint(address, id, amount, '0x').send({ from: accounts[0], value: totalFee })
        .on('receipt', (receipt) => {
            alert('Mint successful');
        })
        .on('error', (error) => {
            console.error(error);
            alert('Mint failed');
        });
});

document.getElementById('balanceForm').addEventListener('submit', async (event) => {
    event.preventDefault();
    const address = document.getElementById('balanceAddress').value;
    const id = document.getElementById('balanceId').value;

    contract.methods.balanceOf(address, id).call()
        .then((balance) => {
            document.getElementById('balanceResult').innerText = `Balance: ${balance}`;
        })
        .catch((error) => {
            console.error(error);
            alert('Query failed');
        });
});
